#include "libft.h"

int	ft_strncmp(const char *str1, const char *str2, int n)
{
	int	i;

	i = 0;
	while (str1[i] != '\0' && i < n)
	{
		if (str1[i] < str2[i])
			return (-1);
		else if (str1[i] > str2[i])
			return (1);
		i++;
	}
	return (0);
}
